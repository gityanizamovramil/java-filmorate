CREATE TABLE IF NOT EXISTS USERS
(
    USER_ID   BIGINT PRIMARY KEY AUTO_INCREMENT,
    LOGIN     VARCHAR,
    USER_NAME VARCHAR,
    EMAIL     VARCHAR,
    BIRTHDAY  DATE
);

CREATE TABLE IF NOT EXISTS USER_FRIENDS
(
    FRIENDSHIP_ID BIGINT PRIMARY KEY AUTO_INCREMENT,
    USER_ID       BIGINT REFERENCES USERS (USER_ID),
    FRIEND_ID     BIGINT REFERENCES USERS (USER_ID)
);

CREATE TABLE IF NOT EXISTS GENRES
(
    GENRE_ID   INT PRIMARY KEY,
    GENRE_NAME VARCHAR
);

CREATE TABLE IF NOT EXISTS RATINGS
(
    RATING_ID   INT PRIMARY KEY,
    RATING_NAME VARCHAR
);

CREATE TABLE IF NOT EXISTS FILMS
(
    FILM_ID      BIGINT PRIMARY KEY AUTO_INCREMENT,
    FILM_NAME    VARCHAR,
    DESCRIPTION  VARCHAR,
    RELEASE_DATE DATE,
    DURATION     INT,
    RATING       INT REFERENCES RATINGS (RATING_ID)
);

CREATE TABLE IF NOT EXISTS FILM_GENRES
(
    FILM_GENRE_ID BIGINT PRIMARY KEY AUTO_INCREMENT,
    FILM_ID       BIGINT REFERENCES FILMS (FILM_ID),
    GENRE_ID      INT REFERENCES GENRES (GENRE_ID)
);

CREATE TABLE IF NOT EXISTS FILM_USER_LIKES
(
    LIKE_ID BIGINT PRIMARY KEY AUTO_INCREMENT,
    FILM_ID BIGINT REFERENCES FILMS (FILM_ID),
    USER_ID BIGINT REFERENCES USERS (USER_ID)
);

